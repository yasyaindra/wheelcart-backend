        //
        $transactions = Transaction::where('user_id', auth()->user()->id)->get();

        $data = [];

        foreach($transactions as $transaction){
            array_push($data, ($transaction->product->price * $transaction->quantity));
        };

        $data = array_sum($data);
        // dd($data);

        return view('cart', ['transactions' => $transactions, 'total' => $data]);
    }

